#include "42-Libft/libft.h"

int	ft_ispath(char *file)
{
	if (ft_strncmp("./", file, 2) == 0 \
		|| ft_strncmp("../", file, 3) == 0 \
		|| ft_strncmp("/", file, 1) == 0)
		return (1);
	return (0);
}
